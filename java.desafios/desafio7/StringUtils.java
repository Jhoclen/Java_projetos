package desafio7;

public class StringUtils {
    
    public void concat(String s1, String s2){
        System.out.println(s1 + s2);

    }
    public void concat(String s1, int s2){
        System.out.println(s1 + Integer.toString(s2));

    }
    public void concat(String s1, String s2, int s3){
        System.out.println(s1 + s2 + Integer.toString(s3));

    }
}
